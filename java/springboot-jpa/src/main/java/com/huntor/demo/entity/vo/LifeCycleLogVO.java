package com.huntor.demo.entity.vo;

public class LifeCycleLogVO {

	private Integer life_cycle_id;
	private String life_cycle_name;
	private Integer out_number;
	private String input_date;

	public Integer getLife_cycle_id() {
		return life_cycle_id;
	}

	public void setLife_cycle_id(Integer life_cycle_id) {
		this.life_cycle_id = life_cycle_id;
	}

	public String getLife_cycle_name() {
		return life_cycle_name;
	}

	public void setLife_cycle_name(String life_cycle_name) {
		this.life_cycle_name = life_cycle_name;
	}

	public Integer getOut_number() {
		return out_number;
	}

	public void setOut_number(Integer out_number) {
		this.out_number = out_number;
	}

	public String getInput_date() {
		return input_date;
	}

	public void setInput_date(String input_date) {
		this.input_date = input_date;
	}

}
